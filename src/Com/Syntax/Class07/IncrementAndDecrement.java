package Com.Syntax.Class07;

public class IncrementAndDecrement {

	public static void main(String[] args) {
		
		int num=90;
		num=num+1;
		
		num+=1;
		
		System.out.println(num);
		
		num++; //adding 1
		System.out.println(num);
		
		int num1=100;
		num1-=1;//subtract 1
		
		num1--;  // increments and decrements operators can only be used only with  1 thats all
		
		System.out.println(num1);
		
		System.out.println("-------------------");
		
		System.out.println("hello");
		System.out.println("hello");
		System.out.println("hello");
		System.out.println("hello");
		System.out.println("hello");
		
		int time =13;
		if (time>12) {
			
			System.out.println("good day");
			
			while (time>12) {
				
				
				System.out.println("good day");
				break;
			}
		}

	}

}
